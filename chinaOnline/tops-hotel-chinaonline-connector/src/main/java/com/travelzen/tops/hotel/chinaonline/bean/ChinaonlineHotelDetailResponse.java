package com.travelzen.tops.hotel.chinaonline.bean;

public class ChinaonlineHotelDetailResponse {
	
	private String hotelName;
	
	private Integer hotelStart;
	
	private String hotelAddress;
	
	private String hotelPostcode;
	
	public String getHotelName() {
		return hotelName;
	}

	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}

	public Integer getHotelStart() {
		return hotelStart;
	}

	public void setHotelStart(Integer hotelStart) {
		this.hotelStart = hotelStart;
	}

	public String getHotelAddress() {
		return hotelAddress;
	}

	public void setHotelAddress(String hotelAddress) {
		this.hotelAddress = hotelAddress;
	}

	public String getHotelPostcode() {
		return hotelPostcode;
	}

	public void setHotelPostcode(String hotelPostcode) {
		this.hotelPostcode = hotelPostcode;
	}


}
